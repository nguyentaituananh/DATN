import React, { useEffect, useState } from "react";
import {
  Tag,
  Spin,
  message,
  Button,
  Space,
  Table,
  Modal,
  Form,
  Input,
  Select,
  Checkbox,
} from "antd";
import dayjs from "dayjs";
import type { ColumnsType } from "antd/es/table";
import { User } from "../../../types";
import {
  getUsers,
  deleteUser,
  updateUser,
  createUser,
} from "../../../api/userApi";

const { Option } = Select;

const UserList: React.FC = () => {
  const [data, setData] = useState<User[]>([]);
  const [loading, setLoading] = useState(false);
  const [form] = Form.useForm();
  const [isModalOpen, setIsModalOpen] = useState(false);
  const [editingUser, setEditingUser] = useState<User | null>(null);
  const [showPassword, setShowPassword] = useState(false); // ‚úÖ Toggle hi·ªán m·∫≠t kh·∫©u

  const fetchUsers = async () => {
    setLoading(true);
    try {
      const res = await getUsers();
      const users = res.data.map((user) => ({
        key: user.id,
        ...user,
      }));
      setData(users);
    } catch (err) {
      message.error("Kh√¥ng th·ªÉ t·∫£i danh s√°ch ng∆∞·ªùi d√πng!");
    } finally {
      setLoading(false);
    }
  };

  useEffect(() => {
    fetchUsers();
  }, []);

  const handleDelete = async (id: string) => {
    try {
      await deleteUser(id);
      message.success("Xo√° th√†nh c√¥ng");
      fetchUsers();
    } catch (error) {
      message.error("Xo√° th·∫•t b·∫°i");
    }
  };

  const openAddModal = () => {
    setEditingUser(null);
    setShowPassword(false);
    form.resetFields();
    setIsModalOpen(true);
  };

  const openEditModal = (user: User) => {
    setEditingUser(user);
    setShowPassword(false);
    form.setFieldsValue(user);
    setIsModalOpen(true);
  };

  const handleSubmit = async (values: any) => {
    try {
      if (editingUser) {
        const updatedData = { ...values };
        if (!values.password) {
          delete updatedData.password;
        }
        await updateUser(editingUser.id, updatedData);
        message.success("C·∫≠p nh·∫≠t th√†nh c√¥ng");
      } else {
        await createUser({
          ...values,
          createdAt: new Date().toISOString(),
        });
        message.success("Th√™m ng∆∞·ªùi d√πng th√†nh c√¥ng");
      }
      setIsModalOpen(false);
      form.resetFields();
      fetchUsers();
    } catch (error) {
      message.error("L∆∞u th·∫•t b·∫°i");
    }
  };

  const columns: ColumnsType<User> = [
    {
      title: "üë§ H·ªç t√™n",
      dataIndex: "name",
      align: "center",
      render: (text) => <span className="font-semibold">{text}</span>,
    },
    {
      title: "üìß Email",
      dataIndex: "email",
      align: "center",
      render: (text) => (
        <span className="text-blue-600 font-medium">{text}</span>
      ),
    },
      {
    title: "üîë M·∫≠t kh·∫©u", // ‚úÖ M·ªõi th√™m
    dataIndex: "password",
    align: "center",
    render: (text) => (
      <span className="text-gray-500 font-mono">{text}</span>
    ),
  },
    {
      title: "üîê Vai tr√≤",
      dataIndex: "role",
      align: "center",
      render: (role) => (
        <Tag
          color={role === "Admin" ? "purple" : "blue"}
          className="rounded-md"
        >
          {role}
        </Tag>
      ),
    },
    {
      title: "üìÖ Ng√†y t·∫°o",
      dataIndex: "createdAt",
      align: "center",
      render: (date) => dayjs(date).format("DD/MM/YYYY"),
    },
    {
      title: "üõ†Ô∏è Thao t√°c",
      dataIndex: "action",
      align: "center",
      render: (_, record) => (
        <Space>
          <Button type="primary" onClick={() => openEditModal(record)}>
            S·ª≠a
          </Button>
          <Button danger onClick={() => handleDelete(record.id)}>
            Xo√°
          </Button>
        </Space>
      ),
    },
  ];

  return (
    <div className="p-6 w-full">
      <div className="bg-white shadow-xl rounded-2xl p-6">
        <div className="flex justify-between items-center mb-4">
          <h2 className="text-2xl font-bold text-gray-800">
            üìã Danh s√°ch ng∆∞·ªùi d√πng
          </h2>
          <Button type="primary" onClick={openAddModal}>
            ‚ûï Th√™m ng∆∞·ªùi d√πng
          </Button>
        </div>

        {loading ? (
          <div className="flex justify-center items-center h-40">
            <Spin size="large" />
          </div>
        ) : (
          <Table
            columns={columns}
            dataSource={data}
            pagination={false}
            bordered
            className="rounded-xl overflow-hidden"
          />
        )}
      </div>

      <Modal
        title={editingUser ? "üìù S·ª≠a ng∆∞·ªùi d√πng" : "‚ûï Th√™m ng∆∞·ªùi d√πng"}
        open={isModalOpen}
        onCancel={() => setIsModalOpen(false)}
        onOk={() => form.submit()}
        okText="L∆∞u"
        cancelText="Hu·ª∑"
      >
        <Form
          form={form}
          layout="vertical"
          onFinish={handleSubmit}
          initialValues={{ role: "User" }}
        >
          <Form.Item
            name="name"
            label="H·ªç t√™n"
            rules={[{ required: true, message: "Vui l√≤ng nh·∫≠p h·ªç t√™n" }]}
          >
            <Input />
          </Form.Item>

          <Form.Item
            name="email"
            label="Email"
            rules={[
              { required: true, message: "Vui l√≤ng nh·∫≠p email" },
              { type: "email", message: "Email kh√¥ng h·ª£p l·ªá" },
            ]}
          >
            <Input />
          </Form.Item>

          <Form.Item
            name="password"
            label="M·∫≠t kh·∫©u"
            rules={
              editingUser
                ? []
                : [{ required: true, message: "Vui l√≤ng nh·∫≠p m·∫≠t kh·∫©u" }]
            }
          >
            <Input
              type={showPassword ? "text" : "password"}
              placeholder={editingUser ? "ƒê·ªÉ tr·ªëng n·∫øu kh√¥ng ƒë·ªïi" : ""}
            />
          </Form.Item>

          <Form.Item>
            <Checkbox
              checked={showPassword}
              onChange={(e) => setShowPassword(e.target.checked)}
            >
              Hi·ªán m·∫≠t kh·∫©u
            </Checkbox>
          </Form.Item>

          <Form.Item
            name="role"
            label="Vai tr√≤"
            rules={[{ required: true, message: "Ch·ªçn vai tr√≤" }]}
          >
            <Select>
              <Option value="Admin">Admin</Option>
              <Option value="User">User</Option>
            </Select>
          </Form.Item>
        </Form>
      </Modal>
    </div>
  );
};

export default UserList;
